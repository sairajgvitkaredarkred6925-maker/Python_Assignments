#Write a python program to perform following operation on list 

#Create and access list elements

list_1=['cat','dog','snake','lion','fish']
print('LIST :',list_1)

print('Accessing element :',list_1[1])

#Add and remove elements 

list_1.append('woodpeacker') 
list_1.remove('fish')

print('Updated list :',list_1)

#Sort list elements 

list_1.sort()

print('Sorted list :',list_1)

#Reverse list elements 

list_1.reverse()

print('Reversed list :',list_1)