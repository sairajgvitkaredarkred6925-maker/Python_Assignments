#Write a python program to perform following operation on tuple

#Create and access tuple elements

tuple_1=('bike','car','bus','train','aeroplane')

print('TUPLE :',tuple_1)

print('Accessing element :',tuple_1[3])

#Nested tuple

tuple_2=(1,2,('cat','dog','snake'),4,5)

print('NESTED TUPLE :',tuple_2)

#Repetition of tuple

tuple_3=('python',)*6

print('REPEATED TUPLE :',tuple_3)

#Concatenation of tuples

tuple_4=('sweet','sour','bitter','salty','spicy')
tuple_5=('apple','lemon','grapefruit','salt','chili')

tuple_6=tuple_4+tuple_5

print('CONCATENATED TUPLE :',tuple_6)